package CoreJava;

public class CompareTo {

	public static void main(String[] args) {
		String s1="hello";
		String s2="hai";
		String s3="helo";
		String s4="hi";
		System.out.println(s1.compareTo(s3));
		System.out.println(s2.compareTo(s3));
		System.out.println(s3.compareTo(s4));
		System.out.println(s4.compareTo(s1));

	}

}
