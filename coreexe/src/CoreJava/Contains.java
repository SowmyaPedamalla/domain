package CoreJava;

public class Contains {

	public static void main(String[] args) {
		String name="welcome to the world";
		System.out.println(name.contains("to the world"));
		System.out.println(name.contains("welcome"));
		System.out.println(name.contains("to world"));
		System.out.println(name.contains("welcome world"));
		

	}

}
