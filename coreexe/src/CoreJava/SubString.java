package CoreJava;

public class SubString {

	public static void main(String[] args) {
		String str=new String ("hai how are you");
		System.out.println("substring starting from index 5:");
		System.out.println(str.substring(5));
		System.out.println("substring starting from index 5 and ending with 15:");
		System.out.println(str.substring(5, 15));
		

	}

}
