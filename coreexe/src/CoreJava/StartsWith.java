package CoreJava;

public class StartsWith {

	public static void main(String[] args) {
		String  str_sample="java strings starts with an example";
		System.out.println("starts With character 'j':" + str_sample.startsWith("j"));
		System.out.println("starts With character 'an':" + str_sample.startsWith("an"));
		System.out.println("starts With character 'strings':" + str_sample.startsWith("strings"));

	}

}
