package coreexe;

public class ConditionalStatements {

	public static void main(String[] args) {
		String genderCode="F";
		String genderDesc=" ";
		if(genderCode=="M") {
			genderDesc="Male";
		

	}else {
		genderDesc="Female";
	}
		System.out.println(genderCode+":"+genderDesc);

}
}
