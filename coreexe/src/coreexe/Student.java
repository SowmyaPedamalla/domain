package coreexe;

public class Student {
	private int id;
	private String name;
	private char sex;
	private String branch;
	private String collegeName;
	private int teluguMarks;
	private int hindiMarks;

	private int mathsMarks;
	private int scienceMarks;
	private int socialMarks;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public char getSex() {
		return sex;
	}
	public void setSex(char sex) {
		this.sex = sex;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getCollegeName() {
		return collegeName;
	}
	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}
	public int getTeluguMarks() {
		return teluguMarks;
	}
	public void setTeluguMarks(int teluguMarks) {
		this.teluguMarks = teluguMarks;
	}
	public int getHindiMarks() {
		return hindiMarks;
	}
	public void setHindiMarks(int hindiMarks) {
		this.hindiMarks = hindiMarks;
	}

	public int getMathsMarks() {
		return mathsMarks;
	}
	public void setMathsMarks(int mathsMarks) {
		this.mathsMarks = mathsMarks;
	}
	public int getScienceMarks() {
		return scienceMarks;
	}
	public void setScienceMarks(int scienceMarks) {
		this.scienceMarks = scienceMarks;
	}
	public int getSocialMarks() {
		return socialMarks;
	}
	public void setSocialMarks(int socialMarks) {
		this.socialMarks = socialMarks;
	}
 

}
